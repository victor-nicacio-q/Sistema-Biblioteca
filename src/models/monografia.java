package models;

public class monografia {
	private int numeroMonografia;
	private String tituloMonografia;
	private String nomeInstituicao;
	private String autores;
	private int mes;
	private int ano;
	private int idLocPub;
	private String tituloPub;
	public int getNumeroMonografia() {
		return numeroMonografia;
	}
	public void setNumeroMonografia(int numeroMonografia) {
		this.numeroMonografia = numeroMonografia;
	}
	public String getTituloMonografia() {
		return tituloMonografia;
	}
	public void setTituloMonografia(String tituloMonografia) {
		this.tituloMonografia = tituloMonografia;
	}
	public String getNomeInstituicao() {
		return nomeInstituicao;
	}
	public void setNomeInstituicao(String nomeInstituicao) {
		this.nomeInstituicao = nomeInstituicao;
	}
	public String getAutores() {
		return autores;
	}
	public void setAutores(String autores) {
		this.autores = autores;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public int getIdLocPub() {
		return idLocPub;
	}
	public void setIdLocPub(int idLocPub) {
		this.idLocPub = idLocPub;
	}
	public String getTituloPub() {
		return tituloPub;
	}
	public void setTituloPub(String tituloPub) {
		this.tituloPub = tituloPub;
	}
	
	
}
