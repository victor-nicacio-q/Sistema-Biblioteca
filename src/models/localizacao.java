package models;

public class localizacao {

	private int idLocalizacao;
	private int numeroFolder;
	private int numeroUsp;
	private String nomeResponsavel;
	private int idComputador;
	private String url;
	private String diretorio;
	private String disco;
	public int getIdLocalizacao() {
		return idLocalizacao;
	}
	public void setIdLocalizacao(int idLocalizacao) {
		this.idLocalizacao = idLocalizacao;
	}
	public int getNumeroFolder() {
		return numeroFolder;
	}
	public void setNumeroFolder(int numeroFolder) {
		this.numeroFolder = numeroFolder;
	}
	public int getNumeroUsp() {
		return numeroUsp;
	}
	public void setNumeroUsp(int numeroUsp) {
		this.numeroUsp = numeroUsp;
	}
	public String getNomeResponsavel() {
		return nomeResponsavel;
	}
	public void setNomeResponsavel(String nomeResponsavel) {
		this.nomeResponsavel = nomeResponsavel;
	}
	public int getIdComputador() {
		return idComputador;
	}
	public void setIdComputador(int idComputador) {
		this.idComputador = idComputador;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDiretorio() {
		return diretorio;
	}
	public void setDiretorio(String diretorio) {
		this.diretorio = diretorio;
	}
	public String getDisco() {
		return disco;
	}
	public void setDisco(String disco) {
		this.disco = disco;
	}
	
	
}
