package db_ep;

public class artigoDePeriodico {
	private String tituloArtigo;
	private String tituloPeriodico;
	private String autores;
	private String editora;
	private int mes;
	private int ano;
	private int paginaInicial;
	private int paginaFinal;
	private int nVolume;
	private int idLocPub;
	private String tituloPub;
	public String getTituloArtigo() {
		return tituloArtigo;
	}
	public void setTituloArtigo(String tituloArtigo) {
		this.tituloArtigo = tituloArtigo;
	}
	public String getTituloPeriodico() {
		return tituloPeriodico;
	}
	public void setTituloPeriodico(String tituloPeriodico) {
		this.tituloPeriodico = tituloPeriodico;
	}
	public String getAutores() {
		return autores;
	}
	public void setAutores(String autores) {
		this.autores = autores;
	}
	public String getEditora() {
		return editora;
	}
	public void setEditora(String editora) {
		this.editora = editora;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public int getPaginaInicial() {
		return paginaInicial;
	}
	public void setPaginaInicial(int paginaInicial) {
		this.paginaInicial = paginaInicial;
	}
	public int getPaginaFinal() {
		return paginaFinal;
	}
	public void setPaginaFinal(int paginaFinal) {
		this.paginaFinal = paginaFinal;
	}
	public int getnVolume() {
		return nVolume;
	}
	public void setnVolume(int nVolume) {
		this.nVolume = nVolume;
	}
	public int getIdLocPub() {
		return idLocPub;
	}
	public void setIdLocPub(int idLocPub) {
		this.idLocPub = idLocPub;
	}
	public String getTituloPub() {
		return tituloPub;
	}
	public void setTituloPub(String tituloPub) {
		this.tituloPub = tituloPub;
	}
	
	
}
